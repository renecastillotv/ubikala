// Helper function to get FAQs for Schema.org
export function getFAQSchemaEN(faqs: FAQ[]) {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    "mainEntity": faqs.map(faq => ({
      "@type": "Question",
      "name": faq.question,
      "acceptedAnswer": {
        "@type": "Answer",
        "text": faq.answer
      }
    }))
  };
}

// Get combined FAQs for a page type
export function getPageFAQsEN(pageType: 'search' | 'location' | 'rent', locationSlug?: string): FAQ[] {
  let faqs: FAQ[] = [...generalFAQsEN];

  if (pageType === 'search') {
    faqs = [
      ...faqs,
      ...firstTimeBuyerFAQsEN.slice(0, 2),
      ...financingFAQsEN.slice(0, 2),
      ...usBuyerFAQsEN.slice(0, 2)
    ];
  }

  if (pageType === 'rent') {
    faqs = [
      ...rentalFAQsEN,
      ...generalFAQsEN.slice(0, 2)
    ];
  }

  if (pageType === 'location' && locationSlug) {
    const locFaqs = locationFAQsEN[locationSlug.toLowerCase()];
    if (locFaqs) {
      faqs = [...locFaqs, ...firstTimeBuyerFAQsEN.slice(0, 2), ...financingFAQsEN.slice(0, 1)];
    }
  }

  return faqs;
}

